交通[編集]

シュヴィーバーディンゲン駅
連邦道（ドイツ語版、英語版） B10号線がシュヴィーバーディンゲンを通っている。このコースはローマ時代からすでにライン川とドナウ川とを結ぶ主要交通路であった。現在は、1962年のバイパス道路建設後は町の中心部を通らず、元の道路の北側を通っている。この道路により、工業地域と住宅地域が分離されている。最寄りのアウトバーンのインターチェンジは、シュヴィーバーディンゲンから約 3 km 南東の A81号線シュトゥットガルト＝ツッフェンハウゼン・インターチェンジである。
シュトローゴイ鉄道は、町の南にあるシュヴィーバーディンゲン駅を通って、コルンタールとハイマーディンゲン（一部の列車はヴァイサハ（ドイツ語版、英語版）まで）との間を定期的に運行している。また、高速鉄道路線マンハイム - シュトゥットガルト線（ファイインゲン・アン・デア・エンツ経由）がシュヴィーバーディンゲンの北東を通っているが、町内に駅はない。さらにシュヴィーバーディンゲンは、ローカル定期バス路線で結ばれており、シュトゥットガルト交通・運賃同盟に加盟している。現在、計画されているルートヴィヒスブルク・シュタットバーンへの接続が検討されている。
シュトゥットガルト国際空港は、約 25 km 離れたラインフェルデン＝エヒターディンゲン（ドイツ語版、英語版）にある。
工業と経済[編集]
かつて「街道沿いの村」と呼ばれていたシュヴィーバーディンゲンでは、往来の活発な帝国道路・交易路に面し、軍隊や商人が行き交うその立地により、昔は旅館や、鍛冶屋、車屋、鞍屋といった職人が重要な意味を持っていた。また、シュヴィーバーディンゲンは1950年代まで農業が主体であった。
1812年にシュヴィーバーディンゲンで最初の工業系企業「ラム・ブラウエライ」が開設された。ここで生産された「ラム＝ブロイ」は、郡域を超えて広く知られていた。1970年代まで従業員数は約60人に増加し、1時間に16,000本が生産された。この中規模なブルワリー業者は、1981年に大規模ブルワリー「シュトゥットガルター・ホーフブロイ」に買収され、稼働を停止した。
1960年代からシュヴィーバーディンゲンの工業とそれに伴う職場の数は飛躍的に発展した。この町の企業、主に自動車部品工場や運輸会社、には現在、合計 7,740人が雇用されている。この街に住む4,053人の非自営業者のうち、シュヴィーバーディンゲン町内で雇用されているのは 942人だけである。2005年の失業率は約 5 % であった。

